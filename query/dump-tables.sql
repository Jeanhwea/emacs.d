SELECT
  '&lpre' || T.TABNAME || '&fsep' || SUBSTR(T.TABCMT, 1, 40) AS ROWDATA
FROM (
  SELECT
    T1.OBJECT_NAME AS TABNAME, --> Table Name
    REPLACE(REPLACE(T2.COMMENTS, CHR(13), ''), CHR(10), '&lsep') AS
      TABCMT --> Table Comments
  FROM
    USER_OBJECTS T1,
    USER_TAB_COMMENTS T2
  WHERE
    T1.OBJECT_NAME = T2.TABLE_NAME
    AND LOWER(T1.OBJECT_TYPE) IN ('table', 'view')
  ORDER BY
    TABNAME) T;
